package testelivraria;

public class Usuario {
	private String nome;
	private int idade;
	private String telefone;
	private String email;
	private String cpf;
	private int id;
	
	public String getNome() {
		return nome;
	}
	public void setNomeUsua(String nome) {
		this.nome = nome;
	}
	public int getIdadeUsua() {
		return idade;
	}
	public void setIdadeUsua(int idade) {
		this.idade = idade;
	}
	public String getTelefoneUsua() {
		return telefone;
	}
	public void setTelefoneUsua(String telefone) {
		this.telefone = telefone;
	}
	public String getEmailUsua() {
		return email;
	}
	public void setEmailUsua(String email) {
		this.email = email;
	}
	public String getCpfUsua() {
		return cpf;
	}
	public void setCpfUsua(String cpf) {
		this.cpf = cpf;
	}
	public int getIdUsua() {
		return id;
	}
	public void setIdUsua(int id) {
		this.id = id;
	}	
}
